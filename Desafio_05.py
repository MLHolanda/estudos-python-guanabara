'''Desafio_05:
Faça um programa que leia um número inteiro e mostre na tela
 o seu sucessor e antecessor.
'''
n = int(input('Qual é o número? '))
n1 = n + 1
n2 = n - 1
print(f'O número escolhido é {n} , seu sucessor é {n1} e seu antecessor é {n2} ' )
print(f'O valor é {n}, seu sucessor é {n + 1} e o seu antecessor é {n - 1}')
